#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	unsigned char	*ptr;

	if (!(ptr = (unsigned char *)malloc(count * size)))
		return (NULL);
	ft_bzero(ptr, count * size);
	return ((void *)ptr);
}
